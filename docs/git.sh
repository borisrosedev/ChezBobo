#!/bin/bash 

git clone #permet de cloner un dépôt distant existant 

git push #permet d'envoyer vers Internet un commit effectué en local 

git add ... #permet d'indexer une modification et la rendre commitable 

git commit -m"feat/fix" #permet de sauvegarder une version de votre dépôt local

git status #permet de vérifier l'état du dépôt local

git pull #permet de rapatrier sur son dépôt des modifications qui se trouvent sur le dépôt distant